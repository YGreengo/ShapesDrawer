
public class Location {
	
	private double x;
	private double y;
	
	public Location (double x, double y) {
		this.x= x;
		this.y = y;
	}
	
	public double getX() {
		return this.x;
	}
	
	public double getY() {
		return this.y;
	}
	public void setLocation(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
}
